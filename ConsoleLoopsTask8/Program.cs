using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleLoopsTask8
{
    class Program
    {
        static void Main(string[] args)
        {
            for (int i = 1; i <= 15; i++)
            {
                if (i <= 4)
                {
                    Console.Write(i);
                }
                else if (i >= 9)
                {
                    Console.Write(i);
                }
                else 
                {
                    Console.Write(i);
                }
            }
            Console.ReadLine();

        }
    }
}

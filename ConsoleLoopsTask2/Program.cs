using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleLoopsTask2
{
    class Program
    {
        static void Main(string[] args)
        {
            for (int i = 1; i <= 9; i++)
            {
                
                Console.WriteLine(i);
                i++;
            }
            Console.ReadLine();
        }
    }
}


